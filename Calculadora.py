# Calculadora
a=int(input("Digite o primeiro número: "))
b=int(input("Digite o segundo número: "))

soma = a+b
sub = a-b
mult = a*b
div = a/b

print("O valor da soma entre {} e {} é igual a {}".format(a,b,soma))
print("A subtração de {} e {} vai ser {}".format(a,b,sub))
print("Se eu pegar {} e dividir por {} o resultado será {}".format(a,b,div))
print("Multiplicação: {} * {} = {}".format(a,b,mult))
print(f"teste para {a} e {b}")
